public class ExemploString {
    public static void main(String[] args) {
       String nome = "Robson";
       String sobrenome = "Souza";
       
       if (nome.equals("robson")){
         System.out.println("Nome é igual robson");
       }else{
        System.out.println("Nome é diferente de robson");
       }

       if (nome.equals(sobrenome)){
         System.out.println("Nome é igual ao sobrenome");
       }else{
         System.out.println("Nome é diferente do sobrenome");
       }


    }          
}
